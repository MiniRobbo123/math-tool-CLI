def factorial(n):
  if n == 0:
    return 1
  elif n < 0:
    return "Math Error"
  elif n % 1 != 0:
    return "Math Error"
  else:
    k = n
    while k > 1:
      n = n * (k-1)
      k -= 1
    return n

