def binToDec(n):
  n = list(str(n))
  if not "1" in n:
    return None
  else:
    while n[0] == "0":
      n = n[1:]
    n = str("".join(n))
    n = int(n,2)
    return n

def decToBin(n):
  n = int(n)
  if type(n) != int:
    return None
  if n < 0:
    return None
  if n % 1 != 0:
    return None
  n = bin(n)
  return n[2:]

