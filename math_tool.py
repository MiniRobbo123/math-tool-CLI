def quadraticSolver(a,b,c):
  if a == 0:
    if b == 0:
      return None, "Invalid input"
    else:
      x1 = -c/b
      x2 = None
      return x1,x2
  else:
    if b**2 - 4*a*c < 0:
      return None, "No real roots"
    else:
      x1 = (-b + (b**2 - 4*a*c)**(1/2))/(2*a)
      x2 = (-b - (b**2 - 4*a*c)**(1/2))/(2*a)
      return x1,x2

